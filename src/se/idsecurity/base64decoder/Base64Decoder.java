package se.idsecurity.base64decoder;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.scene.image.Image;
import javafx.stage.Stage;

/**
 *
 * @author almu
 */
public class Base64Decoder extends Application {
    
    @Override
    public void start(Stage stage) throws Exception {
        Parent root = FXMLLoader.load(getClass().getResource("FXMLDocument.fxml"));
        
        Scene scene = new Scene(root);
        
        stage.getIcons().add(new Image("/se/idsecurity/base64decoder/Cat16.png"));
        stage.getIcons().add(new Image("/se/idsecurity/base64decoder/Cat24.png"));
        stage.getIcons().add(new Image("/se/idsecurity/base64decoder/Cat32.png"));
        stage.getIcons().add(new Image("/se/idsecurity/base64decoder/Cat48.png"));
        
        stage.setTitle("Base64 Decoder");
        
        
        stage.setScene(scene);
        stage.show();
    }

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        launch(args);
    }
    
}
